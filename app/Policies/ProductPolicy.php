    /**
     * Determine whether the user can create models.
     *
     * @param  \App\Models\User  $user
     * @return \Illuminate\Auth\Access\Response|bool
     */
    public function create(User $user)
    {
        $permissions = json_decode($user->role->permissions, true);
        return $permissions['create-product'];
    }

    /**
     * Determine whether the user can update the model.
     *
     * @param  \App\Models\User  $user
     * @param  \App\Models\Product  $product
     * @return \Illuminate\Auth\Access\Response|bool
     */
    public function update(User $user, Product $product)
    {
        $permissions = json_decode($user->role->permissions, true);
        return ($permissions['update-product'] && $user->role->name == 'admin') ||
                    ($permissions['update-product'] && $user->id == $product->user_id);
    }

    /**
     * Determine whether the user can delete the model.
     *
     * @param  \App\Models\User  $user
     * @param  \App\Models\Product  $product
     * @return \Illuminate\Auth\Access\Response|bool
     */
    public function delete(User $user, Product $product)
    {
        $permissions = json_decode($user->role->permissions, true);
        return ($permissions['delete-product'] && $user->role->name == 'admin') ||
            ($permissions['delete-product'] && $user->id == $product->user_id);
    }

